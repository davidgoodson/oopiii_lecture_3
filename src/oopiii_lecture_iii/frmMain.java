    private void loadBioinfo(){
        
        String url = "jdbc:mysql://localhost/iuea";
        String user = "root";
        String pass = "";
        
        try{
            Connection con = DriverManager.getConnection(url, user, pass);
            
            String query = "SELECT * FROM bioinfo";
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query);
            
            DefaultTableModel model = (DefaultTableModel)tblBioinfo.getModel();
            model.setRowCount(0);
            
            if(rs.isBeforeFirst()){
                while(rs.next()){
                    String strSID = rs.getString("sid");
                    String strSurname = rs.getString("surname");
                    String strOthername = rs.getString("othername");
                    String strSex = rs.getString("sex");

                    Object[] obj = {strSID, strSurname, strOthername, strSex};
                    model.addRow(obj);
                }
            }else{
                JOptionPane.showMessageDialog(this, "No Students Registered Yet!");
            }
            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
    
