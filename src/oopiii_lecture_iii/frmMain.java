    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
         String url = "jdbc:mysql://localhost/iuea";
        String user = "root";
        String pass = "";
        
        try{
            Connection con = DriverManager.getConnection(url, user, pass);
            
            String query = "DELETE FROM bioinfo WHERE sid = ?";
            
            int index = tblBioinfo.getSelectedRow();
            
            if(index == -1){
                JOptionPane.showMessageDialog(this, "Please Select a Record to Delete!");
                return;
            }
            
            String strSID = tblBioinfo.getValueAt(index, 0).toString();
            
            PreparedStatement pSt = con.prepareStatement(query);
            pSt.setString(1, strSID);
            
            pSt.execute();
            
            JOptionPane.showMessageDialog(this, "Student Successfully Deleted!");
            
            loadBioinfo();
            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void recordSelectedChanged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_recordSelectedChanged
        // TODO add your handling code here:
        int index = tblBioinfo.getSelectedRow();
             
        String strSID = tblBioinfo.getValueAt(index, 0).toString();
        String strSurname = tblBioinfo.getValueAt(index, 1).toString();
        String strOthername = tblBioinfo.getValueAt(index, 2).toString();
        String strSex = tblBioinfo.getValueAt(index, 3).toString();
        
        txtSurname.setText(strSurname);
        txtOthername.setText(strOthername);
        
        if(strSex.equals("Male"))
            rdbMale.setSelected(true);
        else
            rdbFemale.setSelected(true);
        
        SID = strSID;
        
    }//GEN-LAST:event_recordSelectedChanged

